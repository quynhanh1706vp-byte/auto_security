#!/usr/bin/env bash
set -euo pipefail
cd /home/test/Data/SECURITY_BUNDLE/ui

need(){ command -v "$1" >/dev/null 2>&1 || { echo "[ERR] missing: $1"; exit 2; }; }
need python3; need date; need ss; need awk; need sed; need curl

GW="wsgi_vsp_ui_gateway.py"
[ -f "$GW" ] || { echo "[ERR] missing $GW"; exit 2; }

TS="$(date +%Y%m%d_%H%M%S)"
cp -f "$GW" "${GW}.bak_strip_dash_inline_${TS}"
echo "[BACKUP] ${GW}.bak_strip_dash_inline_${TS}"

python3 - <<'PY'
from pathlib import Path
import re

p = Path("wsgi_vsp_ui_gateway.py")
s = p.read_text(encoding="utf-8", errors="replace")

MARK = "VSP_P1_STRIP_INLINE_DASH_AND_RID_LATEST_WSGIMW_V1"
if MARK in s:
    print("[OK] already patched:", MARK)
    raise SystemExit(0)

addon = r'''
# === %s BEGIN ===
# Goal:
# - OUTERMOST WSGI MW to strip any injected inline dashboard scripts that cause V6E give-up / rid_latest N/A.
# - Ensure /api/vsp/runs response includes rid_latest for schema-compat (even if generated by other MWs).
try:
    import re as _re
    import json as _json

    class _VspStripInlineDashAndRidLatestMW:
        def __init__(self, app):
            self.app = app

        def __call__(self, environ, start_response):
            path = (environ.get("PATH_INFO") or "")
            captured = {"status": None, "headers": None, "exc": None}

            def _sr(status, headers, exc_info=None):
                captured["status"] = status
                captured["headers"] = list(headers or [])
                captured["exc"] = exc_info
                # do not call real start_response yet
                return None

            body_chunks = []
            try:
                it = self.app(environ, _sr)
                for b in it:
                    if b:
                        body_chunks.append(b)
                if hasattr(it, "close"):
                    it.close()
            except Exception:
                # fallback: call inner with real start_response if something unexpected
                return self.app(environ, start_response)

            status = captured["status"] or "200 OK"
            headers = captured["headers"] or []
            body = b"".join(body_chunks)

            # helpers
            def _get_header(name: str):
                ln = name.lower()
                for k, v in headers:
                    if str(k).lower() == ln:
                        return v
                return None

            ct = (_get_header("Content-Type") or "").lower()

            # 1) Fix rid_latest in /api/vsp/runs JSON (schema compat)
            if path == "/api/vsp/runs" and ("application/json" in ct or ct.endswith("+json")):
                try:
                    txt = body.decode("utf-8", errors="replace")
                    j = _json.loads(txt) if txt.strip() else {}
                    if isinstance(j, dict) and ("rid_latest" not in j):
                        items = j.get("items") or []
                        if isinstance(items, list) and items:
                            rid0 = (items[0] or {}).get("run_id")
                            if rid0:
                                j["rid_latest"] = rid0
                                body = _json.dumps(j, ensure_ascii=False).encode("utf-8")
                except Exception:
                    pass

            # 2) Strip injected inline dash scripts on /vsp5 HTML
            if path == "/vsp5" and ("text/html" in ct):
                try:
                    html = body.decode("utf-8", errors="replace")

                    # Remove any <script> blocks that look like the problematic inline dash debug renderer
                    # (these are the ones printing vsp5:32 + rid_latest N/A + give up)
                    def _kill_script(m):
                        whole = m.group(0)
                        inner = m.group(2) or ""
                        # signatures (loose match so grep-miss doesn't matter)
                        sigs = [
                            "rid_latest", "vsp_live_rid", "vsp_rid_latest_badge",
                            "containers/rid", "Chart/container", "[VSP][DASH]"
                        ]
                        if any(s in inner for s in sigs):
                            return ""  # remove this inline script
                        return whole

                    html2 = _re.sub(r"(<script[^>]*>)(.*?)(</script>)", _kill_script, html, flags=_re.S|_re.I)

                    if html2 != html:
                        body = html2.encode("utf-8")
                        # force no-store to avoid browser keeping old injected inline blocks
                        headers = [(k, v) for (k, v) in headers if str(k).lower() not in ("content-length", "cache-control")]
                        headers.append(("Cache-Control", "no-store"))
                        headers.append(("Content-Length", str(len(body))))
                except Exception:
                    pass

            # finalize start_response
            # ensure Content-Length correct
            if not any(str(k).lower()=="content-length" for k,v in headers):
                headers.append(("Content-Length", str(len(body))))
            start_response(status, headers, captured["exc"])
            return [body]

    # Make it OUTERMOST (must be last assignment)
    try:
        application.wsgi_app = _VspStripInlineDashAndRidLatestMW(application.wsgi_app)
    except Exception:
        pass

except Exception:
    pass
# === %s END ===
''' % (MARK, MARK)

p.write_text(s + "\n" + addon, encoding="utf-8")
print("[OK] appended:", MARK)
PY

python3 -m py_compile "$GW" && echo "[OK] py_compile OK"

echo "== restart clean :8910 (nohup only) =="
rm -f /tmp/vsp_ui_8910.lock || true
PID="$(ss -ltnp 2>/dev/null | awk '/:8910/ {print $NF}' | sed -n 's/.*pid=\([0-9]\+\).*/\1/p' | head -n1)"
[ -n "${PID:-}" ] && kill -9 "$PID" || true

: > out_ci/ui_8910.boot.log || true
: > out_ci/ui_8910.error.log || true
nohup /home/test/Data/SECURITY_BUNDLE/ui/.venv/bin/gunicorn wsgi_vsp_ui_gateway:application \
  --workers 2 --worker-class gthread --threads 4 --timeout 60 --graceful-timeout 15 \
  --chdir /home/test/Data/SECURITY_BUNDLE/ui --pythonpath /home/test/Data/SECURITY_BUNDLE/ui \
  --bind 127.0.0.1:8910 \
  --access-logfile out_ci/ui_8910.access.log --error-logfile out_ci/ui_8910.error.log \
  > out_ci/ui_8910.boot.log 2>&1 &

sleep 1.2
curl -sS -I http://127.0.0.1:8910/vsp5 | sed -n '1,12p'

echo "== verify rid_latest on runs =="
curl -sS "http://127.0.0.1:8910/api/vsp/runs?limit=1" | python3 - <<'PY'
import sys,json
j=json.load(sys.stdin)
print("rid_latest =", j.get("rid_latest"))
print("items0.run_id =", (j.get("items") or [{}])[0].get("run_id"))
PY
